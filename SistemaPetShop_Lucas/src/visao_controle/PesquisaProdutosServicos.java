    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        try {
            int linhaSelecionada = jtListaProdutosServicos.getSelectedRow();
            if (linhaSelecionada < 0) {
                JOptionPane.showMessageDialog(this, "Selecione um produto/servi√ßo da lista.", "Aten√ß√£o",
                    JOptionPane.WARNING_MESSAGE);
            } else {
                int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir este produto/servi√ßo?", "Aten√ß√£o",
                    JOptionPane.YES_NO_OPTION);

                if (resposta == JOptionPane.YES_OPTION) {
                    ProdutoServico produtoSelecionado = this.produtospesquisados.get(linhaSelecionada);
                    produtoSelecionado.setAtivo(false);
                    ProdutoServicoDAO dao = new ProdutoServicoDAO();
                    dao.gravar(produtoSelecionado);
                    rodarPesquisa();
                }
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao excluir produto/servi√ßo.", "ERRO",
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbExcluirActionPerformed

